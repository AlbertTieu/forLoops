
public class forLoops
{
    public static void main(String[]args) {
        for (int i = 0; i <= 15; i++) {
            System.out.println("i am " + i);
        }
        for (int j = 10; j != 0; j = j - 2) {
            System.out.println("j is " + j);
        }
        for (int k = 10; k >= 0; k = k - 3) {
            System.out.println("k is " + k);
        }
    }
}
